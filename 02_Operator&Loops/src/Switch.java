
public class Switch {

	public static void main(String[] args) {
		
		int num=200;
		
		switch(num)
		
		{
			case 200:System.out.println("case1");
						System.out.println("wahh bhai tuto હોશીયાર છે.");
						if(num%2==0)
						{
							System.out.println("it's divide by 2");
						}
			break;
			
			case 300:System.out.println("case2");
			break;
			
			case 100:System.out.println("case3");
			break;
			
			default :System.out.println("Bhai sare case galat hai me kya karu tu nikal");
		}
		

	}

}
